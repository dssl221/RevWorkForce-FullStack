-- ---- LEAVE PROCEDURES ----

CREATE OR REPLACE PROCEDURE sp_apply_leave(
    p_employee_id IN NUMBER,
    p_leave_type IN VARCHAR2,
    p_start_date IN DATE,
    p_end_date IN DATE,
    p_reason IN VARCHAR2,
    p_leave_id OUT NUMBER
) AS
    v_days NUMBER;
BEGIN
    v_days := p_end_date - p_start_date + 1;

    INSERT INTO leave_requests (employee_id, leave_type, start_date, end_date, reason, status, applied_date)
    VALUES (p_employee_id, p_leave_type, p_start_date, p_end_date, p_reason, 'PENDING', SYSDATE)
    RETURNING id INTO p_leave_id;

    -- Deduct leave balance
    UPDATE leave_balances SET used_days = used_days + v_days
    WHERE employee_id = p_employee_id AND UPPER(TRIM(leave_type)) = UPPER(TRIM(p_leave_type));

    COMMIT;
END;
/

CREATE OR REPLACE PROCEDURE sp_cancel_leave(p_leave_id IN NUMBER) AS
    v_emp_id NUMBER;
    v_leave_type VARCHAR2(255);
    v_start DATE;
    v_end DATE;
    v_days NUMBER;
BEGIN
    -- Get employee, leave type and days
    SELECT employee_id, leave_type, start_date, end_date INTO v_emp_id, v_leave_type, v_start, v_end
    FROM leave_requests WHERE id = p_leave_id;
    
    v_days := v_end - v_start + 1;

    UPDATE leave_requests SET status = 'CANCELLED' WHERE id = p_leave_id AND status = 'PENDING';

    -- Rollback leave balance
    UPDATE leave_balances SET used_days = used_days - v_days
    WHERE employee_id = v_emp_id AND UPPER(TRIM(leave_type)) = UPPER(TRIM(v_leave_type));

    COMMIT;
END;
/

CREATE OR REPLACE PROCEDURE sp_approve_leave(
    p_leave_id IN NUMBER,
    p_comment IN VARCHAR2,
    p_days IN NUMBER
) AS
BEGIN
    -- Update leave status (balance already deducted on apply)
    UPDATE leave_requests SET status = 'APPROVED', manager_comment = p_comment
    WHERE id = p_leave_id AND status = 'PENDING';

    COMMIT;
END;
/

CREATE OR REPLACE PROCEDURE sp_reject_leave(
    p_leave_id IN NUMBER,
    p_comment IN VARCHAR2
) AS
    v_emp_id NUMBER;
    v_leave_type VARCHAR2(255);
    v_start DATE;
    v_end DATE;
    v_days NUMBER;
BEGIN
    -- Get employee, leave type and days
    SELECT employee_id, leave_type, start_date, end_date INTO v_emp_id, v_leave_type, v_start, v_end
    FROM leave_requests WHERE id = p_leave_id;
    
    v_days := v_end - v_start + 1;

    UPDATE leave_requests SET status = 'REJECTED', manager_comment = p_comment
    WHERE id = p_leave_id AND status = 'PENDING';

    -- Rollback leave balance
    UPDATE leave_balances SET used_days = used_days - v_days
    WHERE employee_id = v_emp_id AND UPPER(TRIM(leave_type)) = UPPER(TRIM(v_leave_type));

    COMMIT;
END;
/

CREATE OR REPLACE PROCEDURE sp_adjust_leave_balance(
    p_employee_id IN NUMBER,
    p_leave_type IN VARCHAR2,
    p_total_days IN NUMBER
) AS
    v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count FROM leave_balances
    WHERE employee_id = p_employee_id AND UPPER(TRIM(leave_type)) = UPPER(TRIM(p_leave_type));

    IF v_count > 0 THEN
        UPDATE leave_balances SET total_days = p_total_days
        WHERE employee_id = p_employee_id AND UPPER(TRIM(leave_type)) = UPPER(TRIM(p_leave_type));
    ELSE
        INSERT INTO leave_balances (employee_id, leave_type, total_days, used_days)
        VALUES (p_employee_id, p_leave_type, p_total_days, 0);
    END IF;
    COMMIT;
END;
/

CREATE OR REPLACE PROCEDURE sp_init_leave_balance(
    p_employee_id IN NUMBER,
    p_leave_type IN VARCHAR2,
    p_total_days IN NUMBER
) AS
BEGIN
    INSERT INTO leave_balances (employee_id, leave_type, total_days, used_days)
    VALUES (p_employee_id, p_leave_type, p_total_days, 0);
    COMMIT;
END;
