-- DEPARTMENTS table
CREATE TABLE departments (
    id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name        VARCHAR2(255) NOT NULL UNIQUE
);

-- USERS table
CREATE TABLE users (
    id                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name                VARCHAR2(255) NOT NULL,
    email               VARCHAR2(255) NOT NULL UNIQUE,
    password            VARCHAR2(255) NOT NULL,
    employee_id         VARCHAR2(255) UNIQUE,
    role                VARCHAR2(20) NOT NULL,
    department          VARCHAR2(255),
    designation         VARCHAR2(255),
    manager_id          NUMBER,
    phone               VARCHAR2(20),
    address             VARCHAR2(500),
    emergency_contact   VARCHAR2(255),
    joining_date        DATE,
    active              NUMBER(1) DEFAULT 1 NOT NULL,
    salary              NUMBER(15,2),
    CONSTRAINT fk_user_manager FOREIGN KEY (manager_id) REFERENCES users(id)
);

-- LEAVE_TYPES table
CREATE TABLE leave_types (
    id              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name            VARCHAR2(255) NOT NULL UNIQUE,
    default_days    NUMBER NOT NULL
);

-- LEAVE_REQUESTS table
CREATE TABLE leave_requests (
    id                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id         NUMBER NOT NULL,
    leave_type          VARCHAR2(255) NOT NULL,
    start_date          DATE NOT NULL,
    end_date            DATE NOT NULL,
    reason              VARCHAR2(500),
    status              VARCHAR2(20) DEFAULT 'PENDING' NOT NULL,
    manager_comment     VARCHAR2(500),
    applied_date        DATE,
    CONSTRAINT fk_leave_employee FOREIGN KEY (employee_id) REFERENCES users(id)
);

-- LEAVE_BALANCES table
CREATE TABLE leave_balances (
    id              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id     NUMBER NOT NULL,
    leave_type      VARCHAR2(255) NOT NULL,
    total_days      NUMBER NOT NULL,
    used_days       NUMBER DEFAULT 0 NOT NULL,
    CONSTRAINT fk_balance_employee FOREIGN KEY (employee_id) REFERENCES users(id),
    CONSTRAINT unq_leave_balance UNIQUE (employee_id, leave_type)
);

-- GOALS table
CREATE TABLE goals (
    id                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id         NUMBER NOT NULL,
    description         VARCHAR2(1000) NOT NULL,
    deadline            DATE,
    priority            VARCHAR2(20) DEFAULT 'MEDIUM',
    progress            NUMBER DEFAULT 0 NOT NULL,
    manager_comment     VARCHAR2(1000),
    CONSTRAINT fk_goal_employee FOREIGN KEY (employee_id) REFERENCES users(id)
);

-- PERFORMANCE_REVIEWS table
CREATE TABLE performance_reviews (
    id                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id         NUMBER NOT NULL,
    deliverables        VARCHAR2(2000),
    accomplishments     VARCHAR2(2000),
    improvements        VARCHAR2(2000),
    self_rating         NUMBER,
    manager_rating      NUMBER,
    manager_feedback    VARCHAR2(2000),
    status              VARCHAR2(20) DEFAULT 'DRAFT' NOT NULL,
    review_year         NUMBER,
    CONSTRAINT fk_review_employee FOREIGN KEY (employee_id) REFERENCES users(id)
);

-- ANNOUNCEMENTS table
CREATE TABLE announcements (
    id              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title           VARCHAR2(255) NOT NULL,
    description     VARCHAR2(2000),
    created_date    DATE DEFAULT SYSDATE
);

-- NOTIFICATIONS table
CREATE TABLE notifications (
    id              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id         NUMBER NOT NULL,
    message         VARCHAR2(500) NOT NULL,
    is_read         NUMBER(1) DEFAULT 0 NOT NULL,
    created_at      TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT fk_notif_user FOREIGN KEY (user_id) REFERENCES users(id)
);

-- HOLIDAYS table
CREATE TABLE holidays (
    id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name    VARCHAR2(255) NOT NULL,
    holiday_date    DATE NOT NULL
);

