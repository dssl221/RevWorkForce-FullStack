CREATE OR REPLACE PROCEDURE sp_register_user(
    p_name IN VARCHAR2,
    p_email IN VARCHAR2,
    p_password IN VARCHAR2,
    p_employee_id IN VARCHAR2,
    p_role IN VARCHAR2,
    p_department IN VARCHAR2,
    p_designation IN VARCHAR2,
    p_manager_id IN NUMBER,
    p_phone IN VARCHAR2,
    p_address IN VARCHAR2,
    p_emergency_contact IN VARCHAR2,
    p_joining_date IN DATE,
    p_salary IN NUMBER,
    p_user_id OUT NUMBER
) AS
BEGIN
    INSERT INTO users (name, email, password, employee_id, role, department, designation,
                       manager_id, phone, address, emergency_contact, joining_date, active, salary)
    VALUES (p_name, p_email, p_password, p_employee_id, p_role, p_department, p_designation,
            p_manager_id, p_phone, p_address, p_emergency_contact, p_joining_date, 1, p_salary)
    RETURNING id INTO p_user_id;
    COMMIT;
END;
/

CREATE OR REPLACE PROCEDURE sp_update_user(
    p_id IN NUMBER,
    p_name IN VARCHAR2,
    p_department IN VARCHAR2,
    p_designation IN VARCHAR2,
    p_phone IN VARCHAR2,
    p_address IN VARCHAR2,
